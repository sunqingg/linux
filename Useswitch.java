public class Useswitch {
    public static void main(String[] args) {
        int a = 3;
        switch (a) {
            case 1:
                System.out.println("我是1");
                break;
            case 3:
                System.out.println("我是3");
                break;
            default:
                System.out.println("我不是我");
                break;
        }
    }
}